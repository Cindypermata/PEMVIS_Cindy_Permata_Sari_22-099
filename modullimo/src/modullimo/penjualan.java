    private void cbpilihActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbpilihActionPerformed
        // TODO add your handling code here:
        String pilihBarang = cbpilih.getSelectedItem().toString();
        
        
        try{
            Statement st = (Statement) mysqlkonek.getConnection().createStatement();
            String query = "SELECT kode, harga FROM tbpenjualan WHERE barang = '" + pilihBarang + "'";
            ResultSet rs = st.executeQuery(query);
            
            if(rs.next()) {
                tfkode.setText(rs.getString("kode"));
                lbharga.setText(rs.getString("harga"));
            }
            
            
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage());
        }
    }//GEN-LAST:event_cbpilihActionPerformed

    private void btnbyarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbyarActionPerformed
        // TODO add your handling code here:
        try {
            int totalHarga = Integer.parseInt(lbtotalharga.getText());
            int totalBayar = Integer.parseInt(tfbayar.getText());

            int kembalian = totalBayar - totalHarga;
            if (kembalian < 0) {
                JOptionPane.showMessageDialog(this, "Uang yang Anda bayarkan kurang");
            } else {
                String kembaliannya = lbkembalian.getText();
                String namanya = tfnama.getText();
                String bayarnya = tfbayar.getText();
                String jumlahpembayaran = lbtotalharga.getText();
                lbkembalian.setText(String.valueOf(kembalian));
                JOptionPane.showMessageDialog(this, "Total pembayaran anda Rp. "+jumlahpembayaran+ " Uang anda Rp. "+bayarnya );
                JOptionPane.showMessageDialog(this, "Selamat "+namanya+ " pembayaran anda sukses & kembalian anda "+kembalian );
                tfnama.setText("");
                tfkode.setText("");
                tfjumlah.setText("");
                tfbayar.setText("");
                lbharga.setText("0");
                lbtotalharga.setText("0");
                lbkembalian.setText("0");
                cbpilih.setSelectedIndex(0);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Masukkan jumlah bayar harus berupa angka.", "Kesalahan Input", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnbyarActionPerformed

