    public void tampil_data(){
        DefaultTableModel tabel1 = new DefaultTableModel();
        tabel1.addColumn("NAMA");
        tabel1.addColumn("KODE");
        tabel1.addColumn("HARGA");
        
        try{
            java.sql.Connection conn = modullimo.koneksi.koneksiDB();
            String query = "select * from tbpenjualan";
            java.sql.PreparedStatement pst = conn.prepareStatement(query);
            ResultSet  rs = pst.executeQuery();
            
            while(rs.next()){
                Object[] row = {
                    rs.getString("barang"),
                    rs.getString("kode"),
                    rs.getString("harga")
                };
                tabel1.addRow(row);
            }
            
            tbdata.setModel(tabel1);
        }catch(Exception e){
            e.printStackTrace();
        }     
    }

