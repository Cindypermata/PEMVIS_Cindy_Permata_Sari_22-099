    private void btnhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhapusActionPerformed
        // TODO add your handling code here:
        int selectedRow = tbdata.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(null, "Pilih baris yang ingin dihapus!");
        } else {
//            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        
            
            int option = JOptionPane.showConfirmDialog(null, "Apakah anda yakin ingin menghapus data ini?", "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);

            if (option == JOptionPane.YES_OPTION) {
                try {
                    String kodeDelete = tbdata.getValueAt(selectedRow, 1).toString();
                    String sql = "DELETE FROM tbpenjualan WHERE kode = ?";
                    java.sql.Connection conn = (java.sql.Connection) modullimo.koneksi.koneksiDB();
                    java.sql.PreparedStatement pst = conn.prepareStatement(sql);
                    pst.setString(1, kodeDelete);
                    pst.executeUpdate();

                    JOptionPane.showMessageDialog(null, "Data berhasil dihapus.");
                    tampil_data();
                    pst.close();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Data gagal dihapus.\n" + e.getMessage());
                    System.out.println(e.getMessage());
                }
            }
        }
    }//GEN-LAST:event_btnhapusActionPerformed

    private void btneditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditActionPerformed
        // TODO add your handling code here:
        int selectedRow = tbdata.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(null, "Pilih baris yang ingin diedit.");
        } else {
            String newBarang = tfBarang.getText();
            String newKode = tfKode.getText();
            
            try {
                BigDecimal newHarga = new BigDecimal(tfHarga.getText());

                int option = JOptionPane.showConfirmDialog(null, "Anda yakin ingin menyimpan perubahan?", "Konfirmasi Edit", JOptionPane.YES_NO_OPTION);

                if (option == JOptionPane.YES_OPTION) {
                    try {
                        String kodeToUpdate = (String) tbdata.getValueAt(selectedRow, 1).toString();
                        String sql = "UPDATE tbpenjualan SET barang = ?, kode = ?, harga = ? WHERE kode = ?";
                        java.sql.Connection conn = (java.sql.Connection) modullimo.koneksi.koneksiDB();
                        java.sql.PreparedStatement pst = conn.prepareStatement(sql);
                        pst.setString(1, newBarang);
                        pst.setString(2, newKode);
                        pst.setBigDecimal(3, newHarga); // Menggunakan setBigDecimal untuk nilai DECIMAL
                        pst.setString(4, kodeToUpdate);
                        pst.executeUpdate();
                        
                        JOptionPane.showMessageDialog(null, "Data berhasil diupdate.");
                        bersihkan();
                        tampil_data();
                        
                        pst.close();
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(null, "Gagal mengupdate data.\n" + e.getMessage());
                        System.out.println(e.getMessage());
                    }
                }
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(null, "Harga harus berupa angka yang valid.");
            }
        }
    }//GEN-LAST:event_btneditActionPerformed

